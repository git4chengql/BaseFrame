package com.ysusoft.frame.sys.changedesk.bean;



public class ChangeDeskBean {
	
	private Integer travel;
	private Integer spot;
	private Integer shopping;
	private Integer hotel;
	private Integer guides;
	public Integer getTravel() {
		return travel;
	}
	public void setTravel(Integer travel) {
		this.travel = travel;
	}
	public Integer getSpot() {
		return spot;
	}
	public void setSpot(Integer spot) {
		this.spot = spot;
	}
	public Integer getShopping() {
		return shopping;
	}
	public void setShopping(Integer shopping) {
		this.shopping = shopping;
	}
	public Integer getHotel() {
		return hotel;
	}
	public void setHotel(Integer hotel) {
		this.hotel = hotel;
	}
	public Integer getGuides() {
		return guides;
	}
	public void setGuides(Integer guides) {
		this.guides = guides;
	}
	
}
